    private void VisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VisualizarActionPerformed

        try{
            DefaultTableModel modelo=(DefaultTableModel)Tabla_Grupos.getModel();
            int filas = modelo.getRowCount();
            for (int i = 0; i<filas; i++){
             modelo.removeRow(0);
            };
           conex = DriverManager.getConnection("jdbc:mysql://localhost/thats_study","root","");
           Statement st=conex.createStatement();
           
           String sqll = "SELECT * FROM grupos";
           ResultSet rs = st.executeQuery(sqll);
           rs.first();
           do{
               String [] fila={rs.getString(1),rs.getString(2)};
               modelo.addRow(fila);
           }while (rs.next());
           
        }catch (SQLException ex){
            Logger.getLogger(Crea_grupos.class.getName()).log(Level.SEVERE,null,ex);
            
        }
        
    }//GEN-LAST:event_VisualizarActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        if (!cod.getText().equals("")){
        try{
            DefaultTableModel modelo=(DefaultTableModel)Tabla_Grupos.getModel();
            int filas = modelo.getRowCount();
            for (int i = 0; i<filas; i++){
             modelo.removeRow(0);
            };
           conex = DriverManager.getConnection("jdbc:mysql://localhost/thats_study","root","");
           Statement st=conex.createStatement();
           
           String sqll = "SELECT * FROM grupos  WHERE ID_Grupo='"+cod.getText()+"'OR Nombre='"+cod.getText()+"'";
           ResultSet rs = st.executeQuery(sqll);
           rs.first();
           if(rs.first()== false){
               JOptionPane.showMessageDialog(null,"Grupo o código no existente","ERROR",JOptionPane.ERROR_MESSAGE);
           }
           do{
               String [] fila={rs.getString(1),rs.getString(2)};
               modelo.addRow(fila);
           }while (rs.next());
           
        }catch (SQLException ex){
            Logger.getLogger(Crea_grupos.class.getName()).log(Level.SEVERE,null,ex);
            
        }
        }else{
            JOptionPane.showMessageDialog(null,"Ingresa código o grupo");
        }
    }//GEN-LAST:event_BuscarActionPerformed

