    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed

        if (nomgrupo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debes digitar el grupo a registrar");
        }else{
        try{
         conex=DriverManager.getConnection("jdbc:mysql://localhost/thats_study","root","");
         Statement st=conex.createStatement();
         st.executeUpdate("INSERT INTO grupos (Nombre) VALUES ('"+nomgrupo.getText()+"')");
         JOptionPane.showMessageDialog(null, "Hecho");
        }catch(SQLException ex){
         Logger.getLogger(Crea_grupos.class.getName()).log(Level.SEVERE,null,ex); 
         JOptionPane.showMessageDialog(null, "No es posible la conexión");
        }      
        }
    }//GEN-LAST:event_CrearActionPerformed

    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
        if (nomgrupo.getText().equals("")&& cod.getText().equals("")&&codigo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No hay datos que limpiar");
        }else{
        codigo.setText("");
        nomgrupo.setText("");
        cod.setText("");
        }
    }//GEN-LAST:event_BorrarActionPerformed

