    private void EntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EntregaActionPerformed
        fechaHora();
        System.out.println("FC"+FechaCierre);
        System.out.println("FA"+FechaActual);
            if (FechaCierre.compareTo(FechaActual) < 0) {
                JOptionPane.showMessageDialog(null, "La fecha de entrega ya expiró","Fecha Límite", JOptionPane.WARNING_MESSAGE);
            } else{

       if (Cod.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Debes llenar el campo de código");
       }if (NomEntrega.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Debes ponerle un nombre a tu entrega");
       }if (Descripcion.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Debes poner una descripción");
       }else{
        int codE = Integer.parseInt(Cod.getText());
        String nombreE = NomEntrega.getText();
        String descripcionE = Descripcion.getText();
        Estudiante EstudianteSeleccionao = (Estudiante) Estudiante.getSelectedItem();
        Asignacion AsignacionSeleccionada = (Asignacion) Asignacion.getSelectedItem();
        int idAsignatura = AsignacionSeleccionada.getIdAsig();
        int codEstudiante = EstudianteSeleccionao.getIdEstudiante();
                
        try{
                File ruta = new File(ruta_archivo);
                conex=DriverManager.getConnection("jdbc:mysql://localhost/thats_study","root","");
                PreparedStatement st=conex.prepareStatement("INSERT INTO entrega (ID_Entrega, Nombre, Descripcion, ID_Asig, Cod_E) VALUES (?,?,?,?,?)");
                st.setInt(1,codE);
                st.setString(2,nombreE);
                st.setString(3,descripcionE);
                st.setInt(4,idAsignatura);
                st.setInt(5,codEstudiante);
                st.executeUpdate();
                guardar_pdf(codE, nombreE,ruta);
                JOptionPane.showMessageDialog(null, "Entrega subida");
            }catch(SQLException ex){
                Logger.getLogger(Crea_grupos.class.getName()).log(Level.SEVERE,null,ex);
                JOptionPane.showMessageDialog(null, "El código de la entrega ya existe, digita otro código");
            } 
       
       }
            }
        
    }//GEN-LAST:event_EntregaActionPerformed

