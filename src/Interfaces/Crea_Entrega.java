    public void cargarestudiante (JComboBox estudiante) {
        try {

            conex = DriverManager.getConnection("jdbc:mysql://localhost/thats_study", "root", "");

            String sql = "SELECT Codigo_Estu, Nombre, Apellido FROM estudiante";
            Statement st = conex.createStatement();
            ResultSet rs = st.executeQuery(sql);
            DefaultComboBoxModel model = new DefaultComboBoxModel();

            while (rs.next()) {
                
                model.addElement(new Estudiante(rs.getInt("Codigo_Estu"),rs.getString("Nombre"),rs.getString("Apellido")));

            }
            estudiante.setModel(model);
        } catch (SQLException e) {
            System.err.println(e);
        }
    }
    
