    private void CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearActionPerformed
        if(nom_materia.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Debes darle un nombre a tu materia");
        }else{
        String nom = nom_materia.getText();
        Profesor docenteSeleccionao = (Profesor) Docente.getSelectedItem();
        
        try{
         conex=DriverManager.getConnection("jdbc:mysql://localhost/thats_study","root","");
         Statement st=conex.createStatement();
         st.executeUpdate ("INSERT INTO materia (Nombre, CodigoP) VALUES ('"+nom+"','"+docenteSeleccionao.getIdDocente()+"')");
         JOptionPane.showMessageDialog(null, "Hecho");
        }catch(SQLException ex){
        Logger.getLogger(Crea_grupos.class.getName()).log(Level.SEVERE,null,ex); 
        JOptionPane.showMessageDialog(null, "No es posible la conexión");
        }
        }
    }//GEN-LAST:event_CrearActionPerformed

