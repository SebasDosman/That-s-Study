    public void cargargrupo(JComboBox profesor) {

        try {

            conex = DriverManager.getConnection("jdbc:mysql://localhost/thats_study", "root", "");

            String sql = "SELECT codigo_profe, nombre,apellido FROM profesor";
            Statement st = conex.createStatement();
            ResultSet rs = st.executeQuery(sql);
            DefaultComboBoxModel model = new DefaultComboBoxModel();

            while (rs.next()) {
                
                model.addElement(new Profesor(rs.getInt("codigo_profe"),rs.getString("nombre"),rs.getString("apellido")));

            }
            profesor.setModel(model);
        } catch (SQLException e) {
            System.err.println(e);
        }

    }
    

